Written on: Friday, April 18th, 2003

Untitled

through the facade of celebrations
    days of honoring people just like us
    that have performed the greatest tasks
    which can be matched by anyone else

there are celebrations of significance
    days of honoring someone unparalleled
    that walked amoungst us
    as if it were behooving

through simple tools He saved the world
through thorns
through nails
came pain
came blood
went life
went sin
all of this to live again

from a perspective above
the lives we live
the concerns we have
and the thoughts we give
to these celebrations that matter so much
which He uses to open our hearts and touch

