Searching...

what is left to give
my heart is only so deep
empty and aching

searching ... for something
or someone ... that cares
that listens
that carefully listens

searching ... for something
or someone ... that loves
that shares
that lovingly shares

wanting ... a friend
that connects
and knows
without being told

that waits
when i am not ready
that persists
when i am timid

that is open
knowing that i am their's
that is safe
knowing that i would not part
that loves
knowing that i am their friend

