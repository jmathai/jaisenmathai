Box

a box ... not too roomy nor spacey
but it will do it's job
it holds beliefs
beliefs which are dear to me
i bring them out
not always on my own accord
they always end up back where they came from
these beliefs are mine
they are never imposed on you
they are never flaunted in your face
never once have they been basis for judgement
never once have they been basis for condemnation
yet they strike a nerve
so i keep them ... in this box

