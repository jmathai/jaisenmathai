Scars

scars.
so deep they're only visible
through my eyes.
the swirls and the patterns
they cover the pain.
what lies beneath.
the tepid water broken like glass
by a single drop.
the ripples distract.
smiles.
nods.
winks.
they hide from you the depths.
uncharted territory.
barren and unclaimed.
unexplored.

but i live there.
put up the tent and stay the night.
staring aimlessly into the dark.
the unknown.
mysterious.
pure.
the only time when everything seems the same.
the only time when everything appears as nothing.
nothingness.
it cannot be taken away.
it is never to be had.
never to be lost.
if nothing were everything.
i would have it all.

