What Measures

would you let me in?
open up and close in around me.
to share your vision.
see through your eyes.
to wade through your thoughts.
feel the heat from your chest.
taste the bitterness in your mouth.
hear your heart palpitate.

would you let me know you?
would you want to know me?
would you hate me for giving up?

would you expect everything?
drop my plans and cater to you.
cease to live to comfort you.
strip down naked to give you warmth.
to deny myself to prolong you.

i would...if you smiled.

