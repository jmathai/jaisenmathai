Written on: Thursday, January 8th, 2004

Slip

it slipped
i felt it leave my grasp
it was so much like slow motion
as i watched it drop to the ground
my heart sank along side of it

the shatter as it hit the pavement
resonates still in my blood
i look with regret
it was something i knew to hold tightly
to never loosen my fingers around

but it's no longer mine to have
the only way to go back in time
is to dwell in my memories
to be there before the change
the friendship preceeding the spill

