Written on: Tuesday, June 17th, 2003

Thoughts of Her

i miss her
it seems so short lived
to never have
to never hold

the few memories which remain
etched by rock on stone
vivid as the blistering heat

an element that captivates
that leaves me stranded
wanting nothing more
than once more

words don't express
but sighs do

i wish i knew
instead i can only feel
inadequate
flawed
something pushed
you away
and it wasn't me

