Written on: Saturday, June 14th, 2003

Sorry

i'm sorry
for replying
for asking and probing
for being nice
for being there

i'm sorry
for sharing
for caring and loving
for inviting you in
for listening

i'm sorry
for sympathizing
for crying and mourning
for allowing this
for prolonging it

i'm sorry
for yelling
for letting you down
for turning my back
for abandoning

i'm sorry
for this
for time and energy
for pain
for loss

i'm sorry
for july, 6th 1999
for today and yesterday
for december 12th
for tomorrow

