Written on: Monday, October 20th, 2003

Illusions of Us

false illusion getting
my confusion started
this delusion invokes
my own seclusion towards
a grim conclusion spoken

in this verse casting
down some curse taking
turns for worse and worser
friendships turned perverse seeking
to immerse oneself
in the inverse of someone else

through this mere facade as if
two peas one pod simulated
through a smile and nod stuffing
my feelings in a wad containing

a similar view as if
i see what you see and
you walk in my shoe but realize
unknown is who i am inside

my own skin as if
i could begin to explain
what lies within or as if
you would care in me or just stay
as you have been pretending to be

a friend in thick and thin

