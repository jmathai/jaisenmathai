Written on: Monday, August 25th, 2003

Familiar Pages

my fingers have traced your features
they know the dos and don'ts
your yeses and nos
the sighs and cringes

i have flipped through the pages
studied the words and sentences
folded over the corners
held on tightly to the binding

i've been here before
but it's different and similar
all at once
the shade, the texture, the shape

nevertheless i feel at home
in my own skin
in your skin
it is one in the same

i see right through you
you're an open book to me

