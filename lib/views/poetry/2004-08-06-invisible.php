Written on: Friday, August 6th, 2004

Invisible

more than something you feel
it's something you become
something you can't escape
it becomes your shadow
a dark empty void by your side
a reminder of everything you wanted to be
a reminder of all that's forgotten
the attemps to define it to others
results in yesterday, the day before, and the day before that
it never changes
it can't
it's not there
