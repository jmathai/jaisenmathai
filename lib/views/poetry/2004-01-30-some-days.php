Written on: Friday, January 30th, 2004

Some Days

some days i crave to be free
i want release from this
to wander once again without this weight

some days i would give it all up
the past and the journey
what we've become

some days i wish i was somebody else
someone without you
alone and cold with the luxury of nothing

some days i think about today
where my life would be now
my mind eventually fades away to reality

some days i know i would be happier
if we had never met
i would trade the joys of the past for a moment of relief

