Written on: Monday, November 24th, 2003

The Air I Breathe

i arrive
without bags or shelter
to your door
asking for a night and a bed

you know my face
you remember the day i deserted you
for my own gain
you remember the day i denied your presence
for my own acceptance
you recognize the person behind the needs

the memories rush between our eyes
i see the glistening light from behind
reflected from my eye and into your tear
shed from your face onto the dirt

i see the door open behind you
i see who i was shed from your memory
who i am now remains etched
as you stretch out your arms
blood drips to meet your tears to meet my hands
i look up at the door and see our father
the invitation remains as you enter through

