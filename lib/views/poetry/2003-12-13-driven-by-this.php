Driven By This

i'm driven by this
not just these words
or phrases or rhymes
what you don't understand
is that what you read today
is what i felt yesterday

you wonder and ask
if everything is ok

people don't seem to understand
that this is a side of me
that i can be completely depressed
with a smile on my face
that i can be in complete bliss
with tears in my eyes

translation?
you wouldn't understand
it's a jaisen thing

i am in a different realm
some other dimension
we are separated
by a one way mirror
my eyes meet yours
while your eyes reflect back to your own

it is by circumstance i presume
neither your fault nor mine
i see the division
i realize the implications
i accept the result

in time i wish you the same

