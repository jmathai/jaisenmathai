Written on: Tuesday, September 9th, 2003

Blueprints

blueprints
sorted and organized
one for each person you meet
size them up
generate a personality
figure them out

without a name
and without a voice
their fate is determined

an innocent face
turned guilty
an unknown mind
sentenced
a flawless spirt
condemned

burn your blueprints
eliminate your presumptions
dismiss your experiences
forget your knowledge

experience someone else
