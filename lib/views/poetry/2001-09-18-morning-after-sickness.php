Written on: Tuesday, September 18th, 2001

Morning-After Sickness

morning after...sickness...after morning
the feeling which dictates my day
as if perpetuating a lonliness i cannot rid
the contentment i feel otherwise is nowhere to be found
this morning it's me ... and my lonliness
a feeling of void, emptiness, incompleteness
in my ear rings
    i'm lonely here inside of me
    deep inside of me
    believe in the light in me
    and i'm lonely here inside of me
    deep inside of me
    reveal the light in me
    i'm lonely here inside of me
    deep inside of me
    i've never ever felt myself this way before
my mind contemplates
what light am i trying to reveal
and how?

