Written on: Friday, October 10th, 2003

Conceit

it's the center
of your universe
it's you

the lenses that life passes though
to get to you
the obstructions that conflict
that keep you from seeing truth
that keep you from seeing outside
of yourself
of your vain
of your pride

look
at the edges
along the perimeter
and into the depths
see what i see?
see what i saw?
it's not the same
something's changed

it's not me
it's not you
it's what i perceived to be you

what i thought i saw
when i saw you in my dreams
who i thought you were

it's easier to not want what i never had
sanity's returned
the glimpses have left
i've been returned to me
