Written on: Monday, January 5th, 2004

In Retrospect

in retrospect
the past days of my life
were filled with questions and doubts
uncertainty and confusion
though it maybe simply a phase of life
it throws me into reverse
traversing backwards and eliminating progress
as if all the steps i took forward were null

now void of direction my soul gives up
waiting for answers
looking for guidance
wanting to hold on to something

unsure of what's next
i remain paralyzed
scared for the first time
of venturing into unknown territory
the same adventures which i once thrived on
have left a bitter taste which makes me cringe

at this point it has become mostly confusion
all directions seem the same
none appealing
being still has never felt so comfortable
being alone has never felt so real
being empty has never defined me
until now

