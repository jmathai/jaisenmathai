Written on: Friday, January 9th, 2004

Deserted

i've been deserted
left stranded and abandoned
you invited me in
and left me alone all in the same breathe

i searched for your voice
even now i continue to listen for it
it's been so long that i forget what the sound was
what is it that i'm listening for?

left to my own devices
confusion surrounds me
so i stop and remain still
it hurts more to digress than to be stagnant

in this stage of life my hope is to minimize pain
at one time i aimed at maximizing comfort
it's become difficult to remember what comfort feels like
but pain is something that has become me

so by eliminating myself
i am able to continue
emotionally i stay afloat
as my life continues to sink
