Written on: Monday, September 22nd, 2003

Slipping

needed
loved
wanted

an aching heart bleeds
a longing soul searches
an unquenched desire remains

useless
unknown
inessential

that aching heart beats
the longing soul rests
these unquenched desires pass

disposable
misunderstood
overlooked

my aching heart surrenders
my longing soul concedes
my unquenched desire resigns

