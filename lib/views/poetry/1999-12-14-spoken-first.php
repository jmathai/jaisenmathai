Spoken First

love ... you said it first.
i felt it last.
i feel it now and it makes me wonder.
which side of you will i remember?
as time passes on, it becomes clear.
you were crueler than you ever were sweet.
sweetness has so many shades.
yours turned out to be a collage of greys.
heartlessness speaks for itself.
louder than the love which precdeded it.
one day your betrayal will be sweet to me.
but til that blood i shed, i lay in agony.

