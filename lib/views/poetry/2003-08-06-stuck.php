Written on: Wednesday, August 6th, 2003

Stuck

a snapshot.
of me...right now.
snap me in half.
shot in the dark.

bend me.
change my perspective.
turn the opaque...transparent.
the transparent...opaque.

take what i have.
give me nothing.

as i morph.
watch me squirm.
molt what i used to be.
to only see the same underneath.

to travel around the world.
only to return to your origin.

some path.
some road.
leads me elsewhere.

