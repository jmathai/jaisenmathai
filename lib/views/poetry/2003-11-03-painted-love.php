Painted Love

nothing describes it
we try and find words
to express what lies inside
we perform random acts of kindness
to show the depth to which it burns

we wade into the water
cold and tepid across the surface
we broke the calm
we started the ripples
they've stretched beyond the horizon

we can't turn back now
looking forward we see two things
the vast sea ahead of us
along with the endless pool of waves we created
facing the choices of remaining or persuing

knowing that only one option exists
we move forward
leaving behind our trails and our secrets
endulging in one another
through the turbulance and the peace

hand in hand, fingers interlocked
this love isn't measured by units
it's measurement is in two hearts
beating together
beating forever

