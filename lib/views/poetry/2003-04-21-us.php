Written on: Monday, April 21st, 2003

Us

you are to me
my better half
my happiness when i am sad
my sadness when i am happy
my eyes when i sleep
my calm when i am anxious
my excitement when i am indifferent

you are for me
the one i can trust
the one i share everything with
the one who sees through my exterior
the one i miss both day and night
the one who let's me be...me

i am to you
grateful for your friendship
beholden for what you've added to my life
obliged for your love

i am with you
closer to being complete
aware of life and what it has to offer
understanding of my surroundings
unafraid of life

you and me
me and you
us

