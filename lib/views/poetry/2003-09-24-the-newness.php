
Written on: Wednesday, September 24th, 2003

The Newness

sudden bliss
you released the snare
unbeknownst to you
you untied the noose

the self-mutilation
of mind and soul
of myself
of who i am

a walk in the clouds
like morning dew
the sugar from the cane
i feel these things
once forgotten
now remembered

i allowed for it
i even invited it
on occasion rejected it
only to search for it

but it's gone
to never have
to never hold

i'm gone
away from this
away from you

